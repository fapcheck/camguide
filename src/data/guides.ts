export function getRelatedGuides(slug: string): Guide[] {
  const guide = getGuideBySlug(slug);
  if (!guide) return [];
  return guide.relatedSlugs
    .map((s) => getGuideBySlug(s))
    .filter((g): g is Guide => g !== undefined);
}
